<template>
  <div class="tab-control">
    <div
      v-for="(item,index) in titles"
      class="tab-control-item"
      :class="{active: index == currentIndex}"
      @click="handleItem(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      default() {
        return []
      }
    }
  },
  data() {
    return {
      currentIndex: 0
    }
  },
  methods: {
    handleItem(index){
      this.currentIndex = index;
      this.$emit('tabClickIndex',index)
    }
  }
}
</script>

